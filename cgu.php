III. Relation entre le Demandeur et le Prestataire

1. Conclusion du contrat

	La Plateforme propose deux techniques de mise en relation des Membres :
	Le Demandeur contacte directement un Prestataire via la Site Internet.
	Le Prestataire contacte directement un Demandeur via le Site Internet.

	La prise de contact se fait exclusivement via la messagerie disponible sur le Site Internet. Par cette messagerie, les Membres se mettent d’accord sur les modalités de réalisation de la Prestation. Par la suite, le contrat de prestation de services est conclu entre le Prestataire et le Demandeur.

