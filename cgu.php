4. Fin de la Prestation
 
Dans le cadre d’une Prestation réalisée par Déplacement, une fois le Prestation réalisée, le Prestataire rentre sur son Profil le code que le Demandeur lui a préalablement transmis.

Cette action traduit la fin de la relation.

	
5. Prix
 

	Le prix est librement fixé par le Demandeur et accepté par le Prestataire.
Le prix est accepté par le Demandeur lors des échanges avec le Prestataire via la messagerie mise en place sur la Plateforme. Cette acceptation formalise l’engagement du Demandeur de payer la Prestation au prix fixé d’un commun accord avec le Prestataire.
 
	
6. Droit de rétractation
 

	Conformément aux articles L. 121-21 et suivants du Code de la consommation, l’acceptation des CGU vaut demande expresse par le Demandeur de commencement immédiat de la Prestation après son accord. 
	En conséquence, le Demandeur pourra user de son droit de rétractation tant que la Prestation ne sera pas entièrement réalisée, moyennant paiement par le Demandeur de la partie du prix correspondant à la partie du service rendu.
	

7. Paiement
 
Le paiement se fait par l’intermédiaire d’un compte virtuel hébergé par le service de paiement Stripe . Il s’agit d’une plateforme tierce de paiement sécurisé mettant en place des portefeuilles virtuels et permettant le dépôt d’une somme d’argent puis la gestion de flux monétiques.

 Au moment de la conclusion du contrat, le Demandeur dépose le prix sur un portefeuille virtuel par virement bancaire, qui reste bloqué jusqu’à la réalisation complète de la Prestation.

	A la fin de la Prestation (voir « Fin de la Prestation »), le prix, déduction faite des frais mentionnés à l’article 9, est automatiquement versé sur le portefeuille virtuel ouvert par le Prestataire.

