	Youthie pourra demander aux Membres, par email ou par téléphone, des informations complémentaires sur leur identité (carte d'identité) et l'identité du porteur de la carte bancaire qui a servi au paiement, en fonction du montant de la commande et de l'adresse du porteur de la carte.

	Les Membres disposent à tout moment d'un droit d'accès et de rectification des données les concernant collectées au moment du paiement.
	
8. Frais

	Youthie prélève 15 % de la somme reçue par le Prestataire sur son portefeuille virtuel, qui couvrent les frais de fonctionnement du site.

	
